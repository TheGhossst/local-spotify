import { NextResponse } from "next/server";
import { SESSION_COOKIE } from "@/app/lib/auth";

export async function POST() {
  const res = NextResponse.json({ ok: true }, {
    headers: { "Cache-Control": "no-store" },
  });
  res.cookies.set(SESSION_COOKIE, "", {
    httpOnly: true,
    sameSite: "lax",
    path: "/",
    maxAge: 0,
  });
  return res;
}
